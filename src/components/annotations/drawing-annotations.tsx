interface DrawingProps {
  drawing: keyof typeof DRAWINGS_DATA;
  strokeWidth?: number;
  color?: string;
  delay?: number;
  duration?: number;
  className?: string;
}

export function Drawing({
  drawing,
  delay = 0,
  duration = 0.6,
  className,
}: DrawingProps) {
  const data = DRAWINGS_DATA[drawing];
  const totalPaths = data.paths.length;

  const transition = useMemo(
    () => ({ ease: [0.16, 1, 0.3, 1] as const, duration }),
    [duration],
  );

  return (
    <svg
      viewBox={`0 0 ${data.viewbox.width} ${data.viewbox.height}`}
      width={data.viewbox.width}
      height={data.viewbox.height}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={cn(
        "absolute overflow-visible fill-none stroke-current stroke-2",
        className,
      )}
      aria-hidden
    >
      {data.paths.map((d, i) => (
        <motion.path
          key={i}
          d={d}
          vectorEffect="non-scaling-stroke"
          initial={{ pathLength: 0, opacity: 0 }}
          animate={{ pathLength: 1, opacity: 1 }}
          transition={{
            ...transition,
            delay: delay + (i / totalPaths) * duration * 0.6,
            opacity: {
              duration: 0.01,
              delay: delay + (i / totalPaths) * duration * 0.6,
            },
          }}
        />
      ))}
    </svg>
  );
}

